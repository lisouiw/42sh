#include "../libft.h"

void				ft_putlong(long n)
{
	unsigned long	i;

	i = n;
	if (n < 0)
	{
		ft_putchar('-');
		i = n * -1;
	}
	if (i >= 10)
		ft_putlong(i / 10);
	ft_putchar_fd((i % 10) + '0', 2);
}
