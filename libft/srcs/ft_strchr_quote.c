#include "../libft.h"

int			ft_strchr_quote(const char *s, char c)
{
	int     i;
	char    *b;

	i = 0;
	b = (char*)s;
	while (b[i] != c && b[i] != '\0')
	{
		if (b[i] == '\\')
			i++;
		i++;
	}
	if (b[i] == c)
		return (i);
	return (i);
}
